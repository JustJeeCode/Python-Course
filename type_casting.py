# Typecasting = the process of converting a variable from one data type to another
#               str(), int(), float(), bool()

name = "JustJee"
age = 25
gpa = 5.5
is_student = True

# Here we are typecasting our variables
name = bool(name)
age = float(age)
gpa = int(gpa)
is_student = str(is_student)

print(is_student)