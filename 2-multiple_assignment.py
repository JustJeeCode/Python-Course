# Multiple assignment = allows us to assign multiple variables at the 
# same time in one line of code.

# Standard variable assignment:
# name = "Jee"
# age = 25
# attractive = True

# Multiple assignment:
# name, age, attractive = "Jee", 25, True 

# print(name)
# print(age)
# print(attractive)

# Another type of multiple assignment
Spongebob = Patrick = Sandy = Squidward = 30

